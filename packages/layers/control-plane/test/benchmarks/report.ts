/**
 * Benchmark Report Generation
 *
 * Generates human-readable markdown reports from benchmark results.
 */

import type { BenchmarkResult } from './framework.js';
import type { Baseline } from './baseline.js';

export interface ReportOptions {
  /** Report title */
  title: string;
  /** Include memory section */
  includeMemory: boolean;
  /** Include system info */
  includeSystem: boolean;
  /** Threshold for highlighting regressions */
  regressionThreshold: number;
}

const DEFAULT_OPTIONS: ReportOptions = {
  title: 'Performance Report',
  includeMemory: true,
  includeSystem: true,
  regressionThreshold: 10,
};

/**
 * Generate a markdown report
 */
export function generateReport(
  results: BenchmarkResult[],
  baseline?: Baseline,
  options?: Partial<ReportOptions>,
): string {
  const opts = { ...DEFAULT_OPTIONS, ...options };
  const lines: string[] = [];

  // Header
  lines.push(`# ${opts.title}\n`);
  lines.push(`Generated: ${new Date().toISOString()}\n`);

  // System info
  if (opts.includeSystem) {
    lines.push('## System Information\n');
    lines.push(`- **Node.js**: ${process.version}`);
    lines.push(`- **Platform**: ${process.platform}`);
    lines.push(`- **Architecture**: ${process.arch}`);
    lines.push(`- **CPUs**: ${require('node:os').cpus().length}`);
    lines.push(`- **Memory**: ${Math.round(require('node:os').totalmem() / 1024 / 1024 / 1024)} GB\n`);
  }

  // Summary table
  lines.push('## Performance Summary\n');
  lines.push('| Benchmark | Mean | Median | P95 | P99 | Ops/sec |');
  lines.push('|-----------|------|--------|-----|-----|---------|');

  for (const result of results) {
    lines.push(
      `| ${result.name} | ${result.meanMs.toFixed(1)}ms | ${result.medianMs.toFixed(1)}ms | ${result.p95Ms.toFixed(1)}ms | ${result.p99Ms.toFixed(1)}ms | ${result.opsPerSecond.toFixed(0)} |`,
    );
  }
  lines.push('');

  // Comparison with baseline
  if (baseline) {
    lines.push('## Comparison with Baseline\n');
    lines.push(`Baseline: ${baseline.version} (${baseline.commit})\n`);
    lines.push('| Benchmark | Current | Baseline | Change |');
    lines.push('|-----------|---------|----------|--------|');

    for (const result of results) {
      const baseResult = baseline.results.find(b => b.name === result.name);

      if (baseResult) {
        const change = ((result.meanMs - baseResult.meanMs) / baseResult.meanMs) * 100;
        const emoji = change > opts.regressionThreshold ? '🔴' : change < -opts.regressionThreshold ? '🟢' : '⚪';
        lines.push(
          `| ${result.name} | ${result.meanMs.toFixed(1)}ms | ${baseResult.meanMs.toFixed(1)}ms | ${emoji} ${change > 0 ? '+' : ''}${change.toFixed(1)}% |`,
        );
      } else {
        lines.push(
          `| ${result.name} | ${result.meanMs.toFixed(1)}ms | - | ⚪ new |`,
        );
      }
    }
    lines.push('');
  }

  // Memory section
  if (opts.includeMemory) {
    lines.push('## Memory Usage\n');
    lines.push('| Benchmark | Memory Delta |');
    lines.push('|-----------|-------------|');

    for (const result of results) {
      lines.push(`| ${result.name} | ${result.memoryDeltaMB.toFixed(2)} MB |`);
    }
    lines.push('');
  }

  // Detailed statistics
  lines.push('## Detailed Statistics\n');

  for (const result of results) {
    lines.push(`### ${result.name}\n`);
    lines.push(`- **Runs**: ${result.runs}`);
    lines.push(`- **Mean**: ${result.meanMs.toFixed(3)} ms`);
    lines.push(`- **Median**: ${result.medianMs.toFixed(3)} ms`);
    lines.push(`- **Std Dev**: ${result.stdDev.toFixed(3)} ms`);
    lines.push(`- **Min**: ${result.minMs.toFixed(3)} ms`);
    lines.push(`- **Max**: ${result.maxMs.toFixed(3)} ms`);
    lines.push(`- **P95**: ${result.p95Ms.toFixed(3)} ms`);
    lines.push(`- **P99**: ${result.p99Ms.toFixed(3)} ms`);
    lines.push(`- **Ops/sec**: ${result.opsPerSecond.toFixed(2)}`);
    lines.push(`- **Memory**: ${result.memoryDeltaMB.toFixed(2)} MB\n`);
  }

  // Footer
  lines.push('---\n');
  lines.push('*Generated by Narada benchmark suite*\n');

  return lines.join('\n');
}

/**
 * Generate a compact report for CI
 */
export function generateCompactReport(
  results: BenchmarkResult[],
  baseline?: Baseline,
): string {
  const lines: string[] = [];

  lines.push('Benchmark Results:');
  lines.push('');

  for (const result of results) {
    let comparison = '';
    if (baseline) {
      const baseResult = baseline.results.find(b => b.name === result.name);
      if (baseResult) {
        const change = ((result.meanMs - baseResult.meanMs) / baseResult.meanMs) * 100;
        const arrow = change > 0 ? '↑' : '↓';
        comparison = ` (${arrow}${Math.abs(change).toFixed(1)}%)`;
      }
    }

    lines.push(
      `  ${result.name}: ${result.meanMs.toFixed(1)}ms${comparison} (${result.opsPerSecond.toFixed(0)} ops/sec)`,
    );
  }

  return lines.join('\n');
}

/**
 * Generate JSON report
 */
export function generateJsonReport(
  results: BenchmarkResult[],
  baseline?: Baseline,
): string {
  const report = {
    timestamp: new Date().toISOString(),
    system: {
      nodeVersion: process.version,
      platform: process.platform,
      arch: process.arch,
    },
    results,
    baseline: baseline
      ? {
          version: baseline.version,
          commit: baseline.commit,
          timestamp: baseline.timestamp,
        }
      : null,
  };

  return JSON.stringify(report, null, 2);
}

/**
 * Print report to console
 */
export function printReport(
  results: BenchmarkResult[],
  baseline?: Baseline,
): void {
  console.log('\n' + '='.repeat(70));
  console.log('BENCHMARK RESULTS');
  console.log('='.repeat(70));

  if (baseline) {
    console.log(`\nBaseline: ${baseline.version} (${baseline.commit})`);
  }

  console.log('\n' + '-'.repeat(70));
  console.log(
    `${'Benchmark'.padEnd(30)} ${'Mean'.padStart(10)} ${'P95'.padStart(10)} ${'Ops/sec'.padStart(12)}`,
  );
  console.log('-'.repeat(70));

  for (const result of results) {
    let changeStr = '';
    if (baseline) {
      const baseResult = baseline.results.find(b => b.name === result.name);
      if (baseResult) {
        const change = ((result.meanMs - baseResult.meanMs) / baseResult.meanMs) * 100;
        const arrow = change > 0 ? '↑' : '↓';
        changeStr = ` ${arrow}${Math.abs(change).toFixed(0)}%`;
      }
    }

    console.log(
      `${result.name.padEnd(30)} ${(result.meanMs.toFixed(1) + 'ms').padStart(10)} ${(result.p95Ms.toFixed(1) + 'ms').padStart(10)} ${result.opsPerSecond.toFixed(0).padStart(12)}${changeStr}`,
    );
  }

  console.log('='.repeat(70) + '\n');
}
